md"""
In order to emulate binary variables in solvers that do not support them, the following constraints can be placed upon the variable, b, that ought to be binary:

- b*b-b == 0 # solutions to (b-0)(b+1)=0 are binary {0,1}
- 0 <= b <= 1 # tight range restriction for speed
"""

# ╔═╡ 689d3bc9-6516-4b3e-876a-f898cf2de900
begin 
	# model = Model(Ipopt.Optimizer)

	# model = Model(CPLEX.Optimizer)
	# set_attribute(model, "CPX_PARAM_EPINT", 1e-8)

	# model = Model(Hypatia.Optimizer)

	model = Model(EAGO.Optimizer)

	######################
	
	factors = [1,2,3,5]
	N = length(factors)

	# floats constrained to {0.0, 1.0}
	# (b - 0)(b - 1) = 0 is solved with b in {0, 1}
	@variable(model, b[1:N])
	@constraint(model, [i = 1:N], 0 <= b[i] <= 1) # Ipopt needs this, just speeds up EAGO
	@constraint(model, [i = 1:N], b[i]*b[i]-b[i] == 0)


	target = 8
	@constraint(model, sum(b[i] for i in 1:N) == 2)
	
	@variable(model, delta) # Ipopt benefitted from bounding this
	@constraint(model, target - sum(b[i]*factors[i] for i in 1:N) <= delta)
	@constraint(model, sum(b[i]*factors[i] for i in 1:N) - target <= delta)

	@objective(model, Min, delta)

	@show model
	optimize!(model)

	value.(b)
end

